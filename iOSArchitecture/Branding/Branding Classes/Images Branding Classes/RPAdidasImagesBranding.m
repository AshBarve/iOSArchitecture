#import "RPAdidasImagesBranding.h"

@implementation RPAdidasImagesBranding

+ (UIImage *)smallLogoImage
{
    // Return the small logo
    return [UIImage imageNamed:@"adidas_small_logo"];
}

+ (UIImage *)bigLogoImage
{
    // Return the big logo
    return [UIImage imageNamed:@"adidas_big_logo"];
}

+ (UIImage *)defaultBackgroundImage
{
    // Return the default background image
    return [UIImage imageNamed:@"adidas_default_background"];
}

@end
