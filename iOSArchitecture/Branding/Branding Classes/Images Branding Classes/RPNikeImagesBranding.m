#import "RPNikeImagesBranding.h"

@implementation RPNikeImagesBranding

+ (UIImage *)smallLogoImage
{
    // Return the small logo
    return [UIImage imageNamed:@"nike_small_logo"];
}

+ (UIImage *)bigLogoImage
{
    // Return the big logo
    return [UIImage imageNamed:@"nike_big_logo"];
}

+ (UIImage *)defaultBackgroundImage
{
    // Return the default background image
    return [UIImage imageNamed:@"nike_default_background"];
}

@end
